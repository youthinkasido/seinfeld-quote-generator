var jump = function (nums, memo = {}) {
  if (nums in memo) return memo[nums];
  if (nums.length === 1) return 0;

  let step = 1;
  let jumps = [];

  while (step <= nums[0] && step < nums.length) {
    jumps.push (jump (nums.slice (step), memo) + 1);
    step++;
  }

  memo[nums] = Math.min (...jumps);
  return memo[nums];
};

